package programs;

public class Byte {

	public static void main(String[] args) {

		byte a = (byte) 128;
		a = (byte) (a + 1);
		System.out.println(a);

		// int range -2,147,483,648 to 2,147,483,647
		int abc = 2147483647;
		System.out.println(abc + 1);

		byte b = (byte) 128;
		b = (byte) ( b + 1);
		System.out.println(b);
	}

}