package programs.String;

public class StringToInteger {

	public static void main(String[] args) {
		// String to Integer
		String s = "2015";
		int b = Integer.parseInt(s);
		System.out.println(b);

		// Integer to String
		int a = 123;
		String a1 = Integer.toString(a);
		System.out.println(a1);
	}
}